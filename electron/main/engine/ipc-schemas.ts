// ─── Automation ────────────────────────────────────────────────────────────

export const AutomationCreateSchema = z.object({
  name: z.string().min(1).max(200),
  description: z.string().max(500).optional(),
  prompt: z.string().min(1).max(20_000),
  schedule: z.enum(['manual', 'hourly', 'daily', 'weekly']),
  timeOfDay: z.string().regex(/^([01]\d|2[0-3]):[0-5]\d$/, 'timeOfDay must be HH:mm in 24-hour format').optional(),
  dayOfWeek: z.number().int().min(0).max(6).optional(),
  model: z.string().max(100).optional(),
})

export const AutomationUpdateSchema = AutomationCreateSchema.partial().extend({
  status: z.enum(['active', 'paused']).optional(),
})

// ─── Helper: parse with friendly error ─────────────────────────────────────

export function safeParseIpc<T>(schema: z.ZodSchema<T>, input: unknown, label: string): T {
  const result = schema.safeParse(input)
  if (!result.success) {
    const issues = result.error.issues
      .slice(0, 3)
      .map((i) => `${i.path.join('.') || 'root'}: ${i.message}`)
      .join('; ')
    throw new Error(`[IPC ${label}] Invalid input — ${issues}`)
  }
  return result.data
}
