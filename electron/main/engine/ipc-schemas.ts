// Runtime validation schemas for IPC inputs.
// These guard the boundary between renderer (untrusted) and main (trusted).
// Use `Schema.parse()` at the start of every ipcMain.handle handler.

import { z } from 'zod'

// ─── Sessions ──────────────────────────────────────────────────────────────

export const SessionMessageSchema = z.object({
  id: z.string().min(1).max(200),
  role: z.enum(['user', 'assistant']),
  // Content is either a string or an array of content blocks; both allowed shapes are loose
  content: z.union([z.string(), z.array(z.unknown())]),
  timestamp: z.number(),
})

export const SessionSchema = z.object({
  id: z.string().min(1).max(200),
  title: z.string().max(500).default('New conversation'),
  createdAt: z.number(),
  updatedAt: z.number(),
  messages: z.array(SessionMessageSchema),
  model: z.string().min(1).max(100),
  workingDir: z.string().max(2000),
  planMode: z.boolean().optional(),
})

export const CreateSessionSchema = z.object({
  model: z.string().min(1).max(100),
  workingDir: z.string().max(2000).optional(),
})

// ─── Skills ────────────────────────────────────────────────────────────────

export const SkillCreateSchema = z.object({
  name: z.string().min(1).max(100).regex(/^[a-z0-9-]+$/i, 'Skill name must be alphanumeric with dashes'),
  description: z.string().min(1).max(500),
  trigger: z.string().min(1).max(500),
  content: z.string().min(1).max(50_000),
})

export const SkillUpdateSchema = SkillCreateSchema.extend({
  id: z.string().min(1).max(200),
})

